import string
def converter(convertin_text):
    convertin_text.replace(" ", "-")
    convertin_text.replace(".", "")
    convertin_text.replace("ž", "z")
    convertin_text.replace("Ž", "z")
    convertin_text.replace("ý", "y")
    convertin_text.replace("Ý", "y")
    convertin_text.replace("á", "a")
    convertin_text.replace("Á", "a")
    convertin_text.replace("č", "c")
    convertin_text.replace("Č", "c")
    convertin_text.replace("ď", "d")
    convertin_text.replace("Ď", "d")
    convertin_text.replace("é", "e")
    convertin_text.replace("É", "e")
    convertin_text.replace("ě", "e")
    convertin_text.replace("Ě", "e")
    convertin_text.replace("í", "i")
    convertin_text.replace("Í", "i")
    convertin_text.replace("ň", "n")
    convertin_text.replace("Ň", "n")
    convertin_text.replace("ó", "o")
    convertin_text.replace("Ó", "o")
    convertin_text.replace("ř", "r")
    convertin_text.replace("Ř", "r")
    convertin_text.replace("š", "s")
    convertin_text.replace("Š", "s")
    convertin_text.replace("ť", "t")
    convertin_text.replace("Ť", "t")
    convertin_text.replace("ú", "u")
    convertin_text.replace("Ú", "u")
    convertin_text.replace("ů", "u")
    convertin_text.replace("Ů", "u")
    convertin_text.replace(string.ascii_uppercase, string.ascii_lowercase)
    return convertin_text
